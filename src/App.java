public class App {
    public static void main(String[] args) throws Exception {
        int tabuada = 5; 
        int x;
        for (x = 0; x<=10; x++){
            System.out.print("Tabuada do " + tabuada);
            System.out.print(" "+ x + " x " + tabuada + " = ");
            System.out.println(x * tabuada);
        }
        
    }
}
